            <div class="main__article--title"><h2>Handicap</h2></div>
            <div class="main__article--top-left"><p>Le handicap est une notion complexe et évolutive. Il désigne l'incapacité d'un individu à vivre et agir pleinement dans son environnement, en raison de déficiences physiques, mentales ou sensorielles.</p>
             <div class="main__article--top-left--partie-2"> À partir de 1980, le terme prend une dimension <span>sociale</span>. On prend conscience que ce qui créer la situation de handicap résulte d'avantage d'un environnement inadapté que de la déficience elle-même.</div>  
           </div>

        </div>

        <div class="main__article--center">
            <div class="main__article--center-right"><p>Le handicap souffre d’une image erronée, se résumant principalement à des personnes en fauteuils roulants. Détrompez-vous, 80 à 85% des situations de handicap sont de nature <span>imperceptible</span>. En effet, comment pourriez-vous devinez qu'untel souffre de diabète et qu'un autre souffre de dyslexie ?</p></div>
        </div>

        <div class="main__article--bottom">
            <div class="main__article--title"><h2>Accessibilité</h2></div>
            <div class="main__article--bottom-left"><p>Elle peut se traduire par l’accès à <span>tous</span> pour tous. Dans notre cadre, elle consiste à mettre les personnes en situation de handicap au même pied d'égalité que les autres. Pour cela on essaie de réduire au minimum les inégalités liées à l'environnement. </p></div>
